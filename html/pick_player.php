			var stat = 0;

			$('#mr').on('click', function(e){
				e.preventDefault();
				stat++;
				$('ul.test').css('left', '-'+stat*33.33+'%');				
			});

			$('#ml').on('click', function(e){
				e.preventDefault();
				if( stat === 0){ return; }
				stat--;
				$('ul.test').css('left', '-'+stat*33.33+'%');				
			});
		});
	</script>
</section>
